const navLinks = document.querySelectorAll('.explore-nav-link');
const contentContainer = document.querySelector('.explore-content');

navLinks.forEach(link => {
  link.addEventListener('click', () => {
    document.querySelector('.explore-nav-link.active').classList.remove('active');
    link.classList.add('active');
    loadContent(link.dataset.category);
  });
});

function loadContent(category) {
  contentContainer.innerHTML = '';
  contentData[category].forEach(item => {
    const explorecard = document.createElement('div');
    explorecard.className = 'explore-card';
    explorecard.innerHTML = `
      <img src="${item.thumbnail}" alt="${item.title}">
      <div class="explore-card-info">
        <div class="explore-card-title">${item.title}</div>
        <div class="explore-card-owner">Owner: ${item.owner}</div>
        <div class="explore-card-views">Views: ${item.views}</div>
        <div class="explore-card-date"> ${item.date}</div>
      </div>
    `;
    contentContainer.appendChild(explorecard);
  });
}

// Load the default category
loadContent('now');
